public class DimensionsOfCase {
    private int height;
    private int width;
    private int length;

    public int getHeight() {
        return height;
    }
    public int getWidth() {
        return width;
    }
    public int getLength() {
        return length;
    }
    public DimensionsOfCase(int height, int width, int length){
        this.height=height;
        this.width=width;
        this.length=length;
    }
}
